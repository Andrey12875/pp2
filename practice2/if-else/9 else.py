# Python Else
# The else keyword catches anything which isn't caught by the preceding conditions.

a = 200
b = 33
if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

#example
x = 5
if x > 10:
    print("big")
else:
    print("small")

n = 0
if n != 0:
    print("non-zero")
else:
    print("zero")

flag = False
if flag:
    print("yes")
else:
    print("no")

age = 15
if age >= 18:
    print("adult")
else:
    print("child")

